void Fuel_powerup::draw(glm::mat4 VP)
{
    Matrices.model = glm::mat4(1.0f);
    glm::mat4 translate = glm::translate(this->position);
    glm::mat4 rotate = glm::mat4(1.0f);

    Matrices.model *= (translate*rotate);
    glm::mat4 MVP = VP * Matrices.model;
    glUniformMatrix4fv(Matrices.MatrixID,1,GL_FALSE,&MVP[0][0]);
    draw3DObject(this->fuel_powerup);
}

void Fuel_powerup::set_position(float x,float y,float z)
{
    this->position = glm::vec3(x,y,z);
}

void Fuel_powerup::tick()
{
        this->position.y -= 0.08;

}

bounding_sphere_t Fuel_powerup::bounding_sphere()
{
    float x,y,z,radius;

    x = this->position.x;
    y = this->position.y;
    z = this->position.z;

    radius = 1.0f;

    bounding_sphere_t bsphere = {x,y,z,radius};

    return bsphere;
}

